package exerciciosFucturaCasa;

public class quatrosPilaresDaOrientaçãoAobjetos {

	public static void main(String[] args) {
		System.out.println("Abstração \n Abstração significa \"esconder\" partes da implementação do objeto expondo apenas uma interface simples para seu uso.");
		
		System.out.println("\nHerança \n A Herança é uma forma de eliminar repetição de código onde, como o próprio nome sugere, um objeto pode herdar características \n(ou seja, propriedades e métodos) de outra classe, sem a necessidade de se reescrever essas mesmas características.");

		System.out.println("\n Encapsulamento\n O encapsulamento é uma das principais técnicas que define a programação orientada a objetos.\n Esse pilar visa a proteção de variáveis importantes dentro de uma classe que não podem ser manipuladas diretamente");
	
		System.out.println("\n Polimorfismo \n Outro ponto essencial na programação orientada a objetos é o chamado polimorfismo. Na natureza, vemos animais que são capazes de\n alterar sua forma conforme a necessidade, e é dessa ideia que vem o polimorfismo na orientação a objetos. Como sabemos, os objetos filhos \n herdam as características e ações de seus “ancestrais”.\r\n" + 
				" Entretanto, em alguns casos, é necessário que as ações para um mesmo método seja diferente. \n Em outras palavras, o polimorfismo consiste na alteração do funcionamento interno de um método herdado de um objeto pai.");
	}

}
